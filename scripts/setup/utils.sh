#!/usr/bin/env bash

# =============================================================================
# Utility Functions
# =============================================================================
# Common helper functions for setup scripts
# =============================================================================

# Constants
export DOTFILES_REPO="https://github.com/MrRoiz/.dotfiles.git"
export DOTFILES_DIR="$HOME/.dotfiles"

print_header() {
  echo ""
  echo "╔══════════════════════════════════════════════════════════════════════════════╗"
  echo "║  $1"
  echo "╚══════════════════════════════════════════════════════════════════════════════╝"
  echo ""
}

print_step() {
  echo "──────────────────────────────────────────────────────────────────────────────────"
  echo ">>> $1"
  echo "──────────────────────────────────────────────────────────────────────────────────"
}

print_substep() {
  echo "    → $1"
}

install_package() {
  print_substep "Installing: $1"
  yay -S $1 --needed
}
